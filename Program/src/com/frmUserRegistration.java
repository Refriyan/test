    public boolean Update() {
        //update of database
        if (txtfname.getText().equals("") || txtusername.getText().equals("") || txtpwd.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please make sure that none of the fields are blank.", "Entry Error", 1);
        } else {
            try {
                String query = "UPDATE tbluser SET realName=?,Surname=?,"
                        + "email=?,phone=?,degree=?,school=?,username=?,"
                        + "user_pass=? WHERE id=?";
                newStock = con.prepareStatement(query);
                newStock.setString(1, txtfname.getText());
                newStock.setString(2, txtlname.getText());
                newStock.setString(3, txtemail.getText());
                newStock.setString(4, txtPhone.getText());
                newStock.setString(5, txtDegree.getText());
                newStock.setString(6, txtSchool.getText());
                newStock.setString(7, txtusername.getText());
                newStock.setString(8, txtpwd.getText());
                newStock.setInt(9, Integer.parseInt(txt_id.getText()));
                if (newStock.executeUpdate() == 1) {
                    JOptionPane.showMessageDialog(this, "Records Updated successfully.", "Entry Error", 1);
                } else {
                    JOptionPane.showMessageDialog(this, "Records Updating failed.", "Entry Error", 1);
                }
                GetRecords();
                // con.close();

            } catch (SQLException a) {
                JOptionPane.showMessageDialog(this, "SQL EXCEPTION fail.", "Entry Error", 1);
                a.printStackTrace();
            } catch (NumberFormatException c) {
                JOptionPane.showMessageDialog(this, "NUMBERFORMAT fail.", "Entry Error", 1);
                c.printStackTrace();
            }
        }
        Clear();

        return true;

    }

