    public boolean getDelete() {
        // TODO add your handling code here:
        if (!txt_id.getText().equals("")) {
            try {

                PreparedStatement ps = con.prepareStatement("DELETE FROM tbluser WHERE id = ?");
                int id = Integer.parseInt(txt_id.getText());
                ps.setInt(1, id);
                ps.executeUpdate();
                GetRecords();
                JOptionPane.showMessageDialog(null, "Records Deleted successfully.");
            } catch (SQLException ex) {
                Logger.getLogger(StockOption.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Product Not Deleted");
            }

        } else {
            JOptionPane.showMessageDialog(null, "Product Not Deleted : No Id To Delete");
        }
        return true;
    }

