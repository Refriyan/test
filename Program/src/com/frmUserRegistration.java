    public boolean GetRecords() {
        try {

            stmt = con.createStatement();
            rs = stmt.executeQuery("SELECT * FROM tbluser");
            model = new DefaultTableModel(records, colHeader);
            jTable1.setModel(model);
            while (rs.next()) {
                model.addRow(new String[]{rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5), rs.getString(6), rs.getString(7), rs.getString(8), rs.getString(9), rs.getString(10), rs.getString(11)});
                jTable1.revalidate();
            }
            // total.setText(""+model.getRowCount());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "SQL EXCEPTION FAILED.", "Entry Error", 1);

        }
        return true;
    }

